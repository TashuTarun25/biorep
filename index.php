        <h3 style="font-weight: bolder;">About the website</h3>
        <p class="about-text">This website has been developed to host data from five esteemed institutes funded by the Department of Biotechnology (DBT).</p>
        <p class="about-text">These institutes are part of India's COVID-19 Bioresource Network, recognized by the Indian Council of Medical Research (ICMR) for their pivotal role in pandemic preparedness and response.</p>
        <p class="about-text">The objective of this website is to provide a centralized platform for accessing the national bioresources crucial for understanding and preparing for future pandemics. During the chaotic times of the COVID-19 outbreak, the Government of India swiftly established these biorepositories on May 23, 2020. The network includes 17 biorepositories situated in institutes under DBT (N=5), CSIR (N=3), and ICMR (N=9), but this website specifically focuses on data from the five DBT-funded institutes.</p>
        <p class="about-text">These repositories have collected and preserved critical longitudinal samples such as serum, nasopharyngeal swabs, plasma, and peripheral blood mononuclear cells from COVID-19 patients. These samples are invaluable for developing validated diagnostics, therapeutics, and vaccines aimed at COVID-19 prevention, control, and treatment. Moreover, they serve as a rich resource for research and development activities to uncover early predictors of disease severity, understand the immunopathogenesis of COVID-19, and more.</p>
        <p class="about-text">Explore our website to learn about the extensive bioresources available at these DBT institutes and how they are being utilized to fortify our preparedness for future health crises.</p>
